.MODEL SMALL
.STACK 200H
.DATA
     M DB ?
     N DB ?
     SODONG DB ? 
     ROW DB 13,10, "$"
.CODE

BEGIN:
MOV AX,@DATA
MOV DS, AX

MOV AH,1
INT 21H
SUB AL,30H
MOV M,AL  

MOV AL,0

MOV AH,1
INT 21H 
SUB AL,30H
MOV N,AL   

MOV AH,9H
LEA DX,ROW
INT 21H   


MOV SODONG,0

IN1DONG:
MOV CH,0
MOV CL,N

L1:

    MOV DL,"*"
    MOV AH, 2H
    INT 21H
LOOP L1

MOV DL, 10
MOV AH,2H
INT 21H

MOV DL, 13
MOV AH,2H
INT 21H

INC SODONG 
MOV BL, SODONG
CMP BL,M
JB IN1DONG
END BEGIN

